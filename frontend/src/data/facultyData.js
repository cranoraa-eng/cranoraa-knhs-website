/** Helper — derive initials from a full name string */
export function getInitials(name) {
  return name
    .split(' ')
    .filter((part) => /^[A-ZÑa-zñ]/.test(part))
    .slice(0, 2)
    .map((part) => part[0].toUpperCase())
    .join('');
}
